    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        String category=kategori.getText();
        String matkul=nama_mk.getText();
        String progamstudi=progstudi.getText();
        String smt= semester.getText();
        try{
            String sql = "INSERT INTO request"
            +"(kategori, nama_mk, prodi,semester)"
            +"VALUES (?,?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost/banksoal_itera","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1,category);
            pst.setString(2,matkul);
            pst.setString(3,progamstudi);
            pst.setString(4,smt);
                    if(category.equals("")){
                JOptionPane.showMessageDialog(null, "Kategori Tidak boleh kosong");
            }else if(matkul.equals("")){
                JOptionPane.showMessageDialog(null, "Mata Kuliah Tidak boleh kosong");                
            }else if(progamstudi.equals("")){
                JOptionPane.showMessageDialog(null, "Program Studi Tidak boleh kosong");         
            }else if(smt.equals("")){
                JOptionPane.showMessageDialog(null, "Semester Tidak boleh kosong");           
            }else{

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Request Berhasil");
            }
        }
            catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

